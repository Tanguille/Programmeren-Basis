using System;

namespace ConsoleApp1
{
    class veelvoudenvan9
    {
        static void Main(string[] args)
        {
            int maaltafel = 9;
            
            for (int i = 1; i <= 10 ; i++)
            {                
                int resultaat = i * maaltafel;
                Console.WriteLine(i + " x " + maaltafel + " = " + resultaat);
            }    
        }
    }
}

    


